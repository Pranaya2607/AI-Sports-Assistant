def normalize_equipment_name(name: str) -> str:
    cleaned = (name or "").strip().lower()
    for key in EQUIPMENT_DATABASE:
        if key.lower() == cleaned:
            return key
    for key in EQUIPMENT_DATABASE:
        if cleaned in key.lower() or key.lower() in cleaned:
            return key
    return name.strip().title() if name else "Unknown Equipment"
